func TestApplyTrustScoreToMatching(t *testing.T) {
	// High trust = boost
	highTrust := TrustScore{OverallScore: 80}
	adjusted := ApplyTrustScoreToMatching(0.5, highTrust)
	if adjusted <= 0.5 {
		t.Errorf("high trust should boost score, got %.3f", adjusted)
	}
	// Low trust = penalty
	lowTrust := TrustScore{OverallScore: 10}
	adjusted = ApplyTrustScoreToMatching(0.5, lowTrust)
	if adjusted >= 0.5 {
		t.Errorf("low trust should reduce score, got %.3f", adjusted)
	}
}

func TestIsTrustApprovedForDeal(t *testing.T) {
	good := TrustScore{OverallScore: 60, DisputeRate: 0.05}
	if !IsTrustApprovedForDeal(good) {
		t.Error("good trust should be approved")
	}
	badScore := TrustScore{OverallScore: 20, DisputeRate: 0.05}
	if IsTrustApprovedForDeal(badScore) {
		t.Error("low overall score should be rejected")
	}
	badDispute := TrustScore{OverallScore: 60, DisputeRate: 0.3}
	if IsTrustApprovedForDeal(badDispute) {
		t.Error("high dispute rate should be rejected")
	}
}

