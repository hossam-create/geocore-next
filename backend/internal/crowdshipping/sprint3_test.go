// ── 2. SCORING CORRECTNESS ────────────────────────────────────────────────────

func TestScoringWeights(t *testing.T) {
	w := 2.0
	deadline := time.Now().Add(72 * time.Hour)
	trip := Trip{
		OriginCountry: "EG", DestCountry: "AE",
		OriginCity: "Cairo", DestCity: "Dubai",
		DepartureDate: time.Now().Add(12 * time.Hour),
		PricePerKg:    5.0, BasePrice: 3.0, AvailableWeight: 5.0,
	}
	dr := DeliveryRequest{
		PickupCountry: "EG", DeliveryCountry: "AE",
		PickupCity: "Cairo", DeliveryCity: "Dubai",
		Deadline: &deadline, Reward: 20.0, ItemWeight: &w,
	}

	cm := countryScore(trip, dr)
	tm := timeScore(trip, dr)
	pf := priceScore(trip, dr)
	rep := 80.0 // simulate

	score := cm*0.40 + tm*0.25 + (rep/100)*0.20 + pf*0.15

	if cm != 1.0 {
		t.Errorf("country = %.2f, want 1.0", cm)
	}
	if tm <= 0 {
		t.Errorf("time = %.2f, want > 0", tm)
	}
	if pf != 1.0 {
		t.Errorf("price = %.2f, want 1.0", pf)
	}
	if score < 0.60 {
		t.Errorf("total score = %.2f, want >= 0.60", score)
	}
}

